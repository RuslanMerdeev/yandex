    class Solution {
        public int[] maxSlidingWindow(int[] nums, int k) {
            // precheck
            int n = nums.length;
            if (k == 1) {
                return nums;
            }

            // window
            int[] window = Arrays.copyOfRange(nums, 0, k);
            int max = Arrays.stream(window).max().getAsInt();

            // result
            int resultN = n - k + 1;
            int[] result = new int[resultN];
            result[0] = max;
            int pos = 0;

//            max = updateWindow(window, nums[0], nums[k], max);

            // computation
            // [1,-4,3,6,1,7], 3 -> [3,6,6,7]
            // pos = [0,\1,2,3\]
            while(pos < resultN - 1) {
//                result[pos] = findMax(window);//Arrays.stream(window).max().getAsInt();
//                if (pos < resultN - 1) {
                    result[pos+1] = updateWindow(window, nums[pos], nums[pos+k], max);
//                }
                pos++;
            }

            return result;
        }

        int updateWindow(int[] window, int toRemove, int toAdd, int prevMax) {
            if (toRemove != toAdd) {
                int len = window.length;
                for (int i = 0; i < len; i++) {
                    if (window[i] == toRemove) {
                        window[i] = toAdd;
                        if (toAdd > prevMax) {
                            return toAdd;
                        } else {
                            return findMax(window);
                        }
                    }
                }
            }
            return prevMax;
        }

        int findMax(int[] window) {
            int max = Integer.MIN_VALUE;
            for (int num : window) {
                if (num > max) {
                    max = num;
                }
            }
            return max;
        }
    }

